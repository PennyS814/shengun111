// ─── 增强型蓍草起卦模拟（确保不同时间/问题产生不同卦象）──

/**
 * 高熵随机数生成器（每次调用返回 [0,1) 区间的浮点数）
 * 混合了 crypto 随机值、高精度时间戳和自增计数器，确保每次调用结果唯一且分布均匀。
 */
(function() {
  let callCounter = 0;
  const getRandom = function() {
    callCounter = (callCounter + 1) & 0x7fffffff;  // 防止过大，保留正整型
    // 1. 高精度时间（微秒级）
    const now = performance ? performance.now() : Date.now();
    // 2. 尝试使用 crypto 生成真正不可预测的随机数
    let cryptoRand = 0;
    if (typeof window !== 'undefined' && window.crypto && window.crypto.getRandomValues) {
      const buf = new Uint32Array(1);
      window.crypto.getRandomValues(buf);
      cryptoRand = buf[0] / 4294967296;  // 转为 [0,1)
    } else {
      // 降级方案：结合 Math.random 与时间/计数器扰动
      cryptoRand = (Math.random() + (now * 1e-6) + callCounter) % 1;
    }
    // 最终值混合微小时间扰动，确保即使在同一毫秒内连续调用也不会重复
    const finalRand = (cryptoRand + (now % 1000) / 1000) % 1;
    return finalRand;
  };

  // 保留原始函数结构，仅替换随机数来源
  window.simulateYarrow = function simulateYarrow() {
    const r = getRandom();
    if (r < 1/16) return 6;       // 老阴 (1/16)
    else if (r < 6/16) return 7;  // 少阳 (5/16)
    else if (r < 13/16) return 8; // 少阴 (7/16)
    else return 9;                // 老阳 (3/16)
  };

  window.generateHexagram = function generateHexagram() {
    const lines = [];        // 从初爻到上爻 (索引0~5)
    const changing = [];
    for (let i = 0; i < 6; i++) {
      const v = window.simulateYarrow();
      lines.push(v === 7 || v === 9 ? 1 : 0);
      changing.push(v === 6 || v === 9 ? 1 : 0);
    }
    // 反转：使顺序变为 上/五/四/三/二/初（符合数据库键顺序）
    const primary = [...lines].reverse();
    const changeRev = [...changing].reverse();
    const changed = primary.map((v, i) => changeRev[i] ? 1 - v : v);
    return { primary, changing: changeRev, changed };
  };
})();

// ─── 丰富离线解读引擎 ─────────────────
function interpretOffline(question, primary, changed, changing) {
  const pKey = primary.join('');
  const cKey = changed.join('');
  const pHex = HEXAGRAMS[pKey] || { name: '未知', text: '' };
  const cHex = HEXAGRAMS[cKey] || { name: '未知', text: '' };

  const movingIdx = [];   // 动爻位置索引（0=上爻,5=初爻）
  changing.forEach((v, i) => { if (v) movingIdx.push(i); });
  const moveCount = movingIdx.length;

  // 开始组装解读文本
  let advice = [];

  // 1. 问题回顾
  advice.push(`您问：「${question}」\n`);

  // 2. 卦象简述
  advice.push(`本卦为【${pHex.name}】，卦辞曰：${pHex.text}`);
  if (moveCount > 0) {
    advice.push(`动爻：${movingIdx.map(i => ['上','五','四','三','二','初'][i] + '爻').join('、')}。`);
  } else {
    advice.push(`此卦六爻安静，无动爻。`);
  }
  if (moveCount > 0) {
    advice.push(`变卦为【${cHex.name}】，卦辞曰：${cHex.text}`);
  }

  // 3. 动爻爻辞引用（最关键）
  if (moveCount > 0 && pHex.lines) {
    advice.push(`\n—— 动爻精要 ——`);
    movingIdx.forEach(idx => {
      // 数据库 lines 顺序：初九(0)、九二(1)、…、上九(5)、用九(6)
      // idx 对应上爻→初爻，所以需要转换：上爻=5位置 → lines[5]
      const lineIdx = 5 - idx; // 转换
      const lineText = pHex.lines[lineIdx] || '';
      advice.push(`• ${lineText}`);
    });
  }

  // 4. 综合判断
  const yangRatio = primary.filter(v => v === 1).length / 6;

  advice.push(`\n—— 综合启示 ——`);

  if (moveCount === 0) {
    advice.push(`卦象不动，局势稳定。应以本卦卦辞为主，守正待时，顺其自然。`);
  } else if (moveCount >= 5) {
    advice.push(`动爻极多，变数剧烈，往往为人生关键时刻。宜冷静观察，随机应变，不可固执一端。`);
  } else if (moveCount >= 3) {
    advice.push(`动爻过半，事态多变，需注意变卦所示的发展方向。`);
  } else {
    advice.push(`一动或二动，应重点关注动爻爻辞，它为当前问题的核心指向。`);
  }

  // 根据阴阳平衡给予建议
  if (yangRatio > 0.7) {
    advice.push(`本卦阳气过盛，刚强有余，需防亢龙有悔，适时收敛锋芒。`);
  } else if (yangRatio < 0.3) {
    advice.push(`本卦阴气较重，宜守不宜攻，以柔克刚，耐心等待时机。`);
  } else {
    advice.push(`阴阳平衡，可进可退，顺应时势而行。`);
  }

  // 变卦气势
  if (moveCount > 0) {
    const cYang = changed.filter(v => v === 1).length / 6;
    if (cYang > 0.7) advice.push(`变卦阳气旺盛，未来将转向积极奋发之象。`);
    else if (cYang < 0.3) advice.push(`变卦阴柔，最终宜低调行事，以退为进。`);
    else advice.push(`变卦平和，终局稳定，可稳步推进。`);
  }

  return advice.join('\n');
}

